function proses_Callback(hObject, eventdata, handles)
% hObject    handle to proses (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
x = xlsread('saw1.xlsx');%memanggil data tadi ke sini dari text 4
k = xlsread('bobot.xlsx');

[m n]=size (x); %matriks m x n dengan ukuran sebanyak variabel x (input)
R=zeros (m,n); %membuat matriks R, yang merupakan matriks kosong
Y=zeros (m,n); %membuat matriks Y, yang merupakan titik kosong
for j=1:n,
 if k(j)==1, %statement untuk kriteria dengan atribut keuntungan
  R(:,j)=x(:,j)./max(x(:,j));
 else
  R(:,j)=min(x(:,j))./x(:,j);
 end;
  set(handles.uitable5,'Data',R);  %gak terlalu paham mas hee soal belum ada yang jelasin tapi alhamdulilah bisa buat kek gini karna mbah google
end;
w = xlsread('atribut.xlsx');
for i=1:m,
 V(i)= sum(w.*R(i,:))
 set(handles.hasil,'Data',V);
 [nilai,urutan]=max(V)
 set(handles.edit1,'string',(nilai)); %mengeluarkan data tertinggi
  set(handles.edit2,'string',(urutan)); %mengeluarkan urutan keberapa
end;



